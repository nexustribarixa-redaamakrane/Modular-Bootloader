void mbl_sutf_init(mbl_sutf_renderer_t *ctx, mbl_framebuffer_t *fb, uint32_t x, uint32_t y, uint32_t color) {
    ctx->framebuffer = fb;
    ctx->cursor_x    = x;
    ctx->cursor_y    = y;
    ctx->origin_x    = x;
    ctx->color       = color;
    ctx->bg_color    = 0x00FFFFFFU; /* transparent background */
    ctx->bold        = false;
}

void mbl_sutf_putchar_base(mbl_sutf_renderer_t *ctx, sucs_char_t cp) {
    if (cp == '\n') {
        ctx->cursor_y += MBL_FONT_HEIGHT;
        ctx->cursor_x = ctx->origin_x;
        return;
    } else if (cp == '\r') {
        ctx->cursor_x = ctx->origin_x;
        return;
    } else if (cp == '\t') {
        ctx->cursor_x += MBL_FONT_WIDTH * 4;
        return;
    }

    if (cp >= 0x20 && cp <= 0x7E) {
        const uint8_t *glyph = g_font8x16[cp - 0x20];
        mbl_framebuffer_blit_glyph(ctx->framebuffer, ctx->cursor_x, ctx->cursor_y, glyph, ctx->color, ctx->bg_color);
        if (ctx->bold) { /* blit secondary offset glyph for bold effect */
            mbl_framebuffer_blit_glyph(ctx->framebuffer, ctx->cursor_x + 1, ctx->cursor_y, glyph, ctx->color, 0x00FFFFFFU);
        }
    }
    ctx->cursor_x += MBL_FONT_WIDTH;
}

void mbl_sutf_putchar_ext(mbl_sutf_renderer_t *ctx, sucs_ex_char_t ex_cp) {
    sucs_char_t cp = 0;
    if (sucs_downcast(ex_cp, &cp)) {
        mbl_sutf_putchar_base(ctx, cp);
    } else {
        /* Fallback glyph for high ExtSUCS codepoints */
        mbl_sutf_putchar_base(ctx, '?');
    }
}

void mbl_sutf_puts_sutf8(mbl_sutf_renderer_t *ctx, const char *sutf8_text) {
    if (!sutf8_text) return;
    const uint8_t *stream = (const uint8_t *)sutf8_text;
    size_t offset = 0;

    while (stream[offset] != '\0') {
        sucs_char_t cp = 0;
        size_t bytes_read = sutf8_decode_char(stream + offset, 6, &cp);
        if (bytes_read == 0) {
            offset++; /* Skip invalid byte */
            continue;
        }
        mbl_sutf_putchar_base(ctx, cp);
        offset += bytes_read;
    }
}

void mbl_sutf_puts_vsutf(mbl_sutf_renderer_t *ctx, const uint8_t *vsutf_stream, size_t stream_size) {
    if (!vsutf_stream) return;
    size_t offset = 0;

    while (offset < stream_size) {
        sucs_ex_char_t ex_cp = 0;
        size_t bytes_read = vsutf_decode(vsutf_stream + offset, stream_size - offset, &ex_cp);
        if (bytes_read == 0) {
            offset++;
            continue;
        }
        mbl_sutf_putchar_ext(ctx, ex_cp);
        offset += bytes_read;
    }
}
